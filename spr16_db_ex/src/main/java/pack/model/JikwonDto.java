package pack.model;

public class JikwonDto {
	private String jikwon_name, jikwon_jik, jikwon_gen, gogek_name, gogek_damsano;

	public String getJikwon_name() {
		return jikwon_name;
	}

	public void setJikwon_name(String jikwon_name) {
		this.jikwon_name = jikwon_name;
	}

	public String getJikwon_jik() {
		return jikwon_jik;
	}

	public void setJikwon_jik(String jikwon_jik) {
		this.jikwon_jik = jikwon_jik;
	}

	public String getJikwon_gen() {
		return jikwon_gen;
	}

	public void setJikwon_gen(String jikwon_gen) {
		this.jikwon_gen = jikwon_gen;
	}

	public String getGogek_name() {
		return gogek_name;
	}

	public void setGogek_name(String gogek_name) {
		this.gogek_name = gogek_name;
	}

	public String getGogek_damsano() {
		return gogek_damsano;
	}

	public void setGogek_damsano(String gogek_damsano) {
		this.gogek_damsano = gogek_damsano;
	}

}
